class Solution{
    //Function to find the leaders in the array.
    static ArrayList<Integer> leaders(int arr[], int n){
        // Your code here
        ArrayList<Integer> ans = new ArrayList<>();
        int max = 0;
        
        for(int i = n - 1 ; i>=0 ; i --){
            int val = arr[i];
            if(val>=max){
                ans.add(val);
            }
            max = Math.max(max,val);
        }
        
        int s =0;
        int e = ans.size() - 1;
        
        while(s <= e){
            int temp = ans.get(s);
            ans.set(s,ans.get(e));
            ans.set(e,temp);
            s++;
            e--;
        }
        return ans;
    }
}
